/**
 * AES Phase 1 — Real Build Test
 *
 * This is the first test where AES produces REAL code, runs REAL tests,
 * and validates REAL output. No simulations.
 *
 * Target: A todo API with CRUD endpoints
 * What happens:
 *   1. Bridge compiled with system context
 *   2. Code generator writes actual TypeScript files to disk
 *   3. Jest runs actual tests against the generated code
 *   4. Validator checks the real diff and test results
 *   5. Artifacts recorded in the registry
 *
 * The "builder" here is a deterministic code generator, not Claude.
 * This proves the pipeline works. Swapping in Claude as the builder
 * is a config change, not an architecture change.
 */

